public class else_if_grade {

	public static void main(String[] args) {
		int marks=70;
		if(marks>=80) { 
			System.out.println("grade a");
		} else if (marks>=60) {
			System.out.println("grade b");
		}	else if(marks>=40) {
			System.out.println("grade c");
		} else {
			System.out.println("fail");
		}
			
		
		
	}
}

