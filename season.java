package ch3;

public class season {

	public static void main(String[] args) {
		int season = 4;
		switch (season) {
		case 1 : System.out.println("summer");
		break;
		case 2 : System.out.println("winter");
		break;
		case 3 : System.out.println("autumn");
		break;
		case 4: System.out.println("moonsoon");
		break;
		default:System.out.println("invalid day");
		}

	}

}