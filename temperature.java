package ch3;

public class temperature {

	public static void main(String[] args) {
		int temp = 45;
		if(temp>45) {
			System.out.println("temp is high");
		}
		else if(temp<=45) {
			System.out.println("temp is moderate");
		}
		else {
			System.out.println("temp is low");
		}

	}

}
